from math import sqrt # Existem diversas funções


def calculate_square_root(number): # retorna a raiz quadrada de um número ex: 16 -> 4
    return sqrt(number)

def calculate_square(number): # retorna o quadrado de um número ex: 4 -> 16
    return number * number

raizQuadrada = print(calculate_square_root(16))
quadradoDoNumero = print(calculate_square(4)) 